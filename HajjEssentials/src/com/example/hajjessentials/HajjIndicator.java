package com.example.hajjessentials;

public class HajjIndicator {

	
	private String indication_id;
	private String indication_title;
	private String indication_detail;
	
	public String getIndication_id() {
		return indication_id;
	}
	public void setIndication_id(String indication_id) {
		this.indication_id = indication_id;
	}
	public String getIndication_title() {
		return indication_title;
	}
	public void setIndication_title(String indication_title) {
		this.indication_title = indication_title;
	}
	public String getIndication_detail() {
		return indication_detail;
	}
	public void setIndication_detail(String indication_detail) {
		this.indication_detail = indication_detail;
	}
}
